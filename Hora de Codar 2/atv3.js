var n1 = prompt("Digite o primeiro número");
var n2 = prompt("Digite o segundo número");
var n3 = prompt("Digite o terceiro número");

if (n1 > n2 && n1 > n3) { document.write("O número " + n1 + " é o maior") }
else if (n2 > n1 && n2 > n3) { document.write("O número " + n2 + " é o maior") }
else if (n3 > n1 && n3 > n2) { document.write("O número " + n3 + " é o maior") }